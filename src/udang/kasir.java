            private void populateComboBox() {
        String sql = "SELECT nm_barang, harga FROM kasir";
        try (PreparedStatement stmt = connection.prepareStatement(sql);
             ResultSet rs = stmt.executeQuery()) {
            while (rs.next()) {
                String namaBarang = rs.getString("nm_barang");
                comboModel.addElement(namaBarang);
            }
            box.setModel(comboModel);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
            
            private void updateHargaSatuanField(String selectedItem) {
    String sql = "SELECT harga FROM kasir WHERE nm_barang = ?";
    try (PreparedStatement stmt = connection.prepareStatement(sql)) {
        stmt.setString(1, selectedItem);
        ResultSet rs = stmt.executeQuery();
        if (rs.next()) {
            int harga = rs.getInt("harga");
            satuan.setText(String.valueOf(harga));
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
}
