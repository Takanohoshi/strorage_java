    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
int totalHarga = Integer.parseInt(total.getText());
int bayarAmount = Integer.parseInt(tbayar.getText());
int kembalianAmount = Integer.parseInt(kembalian.getText());

SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
String tanggalTransaksi = sdf.format(new java.util.Date());

String sql = "INSERT INTO laporan (tanggal_transaksi, total_bayar, nama_kasir) VALUES (?, ?, ?)";

try (PreparedStatement stmt = connection.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS)) {
    stmt.setString(1, tanggalTransaksi);
    stmt.setInt(2, totalHarga);
    stmt.setString(3, "saki");

    int rowsAffected = stmt.executeUpdate();

    if (rowsAffected > 0) {
        ResultSet generatedKeys = stmt.getGeneratedKeys();
        if (generatedKeys.next()) {
            int generatedId = generatedKeys.getInt(1);
            JOptionPane.showMessageDialog(null, "Transaksi berhasil disimpan. ID Transaksi: " + generatedId);
        } else {
            JOptionPane.showMessageDialog(null, "Gagal mendapatkan ID Transaksi.");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Gagal menyimpan transaksi.");
    }
} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menyimpan transaksi.");
}
    }//GEN-LAST:event_simpanActionPerformed


    private int hitungTotalHarga() {
    DefaultTableModel model = (DefaultTableModel) keranjang.getModel();
    int rowCount = model.getRowCount();
    int totalHarga = 0;

    for (int i = 0; i < rowCount; i++) {
        totalHarga += Integer.parseInt(model.getValueAt(i, 4).toString());
    }

    return totalHarga;
}
    
    private void updateTotal() {
    int totalHarga = hitungTotalHarga();
    total.setText(Integer.toString(totalHarga));
}
