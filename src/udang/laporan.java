    private void filterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterActionPerformed
        // TODO add your handling code here:
            java.util.Date startDate = jDateChooser1.getDate();
    java.util.Date endDate = jDateChooser2.getDate();

    // Convert the selected dates to java.sql.Date format
    java.sql.Date sqlStartDate = new java.sql.Date(startDate.getTime());
    java.sql.Date sqlEndDate = new java.sql.Date(endDate.getTime());

    // Clear the existing table data
    DftTblModel_barang.setRowCount(0);

    // Fetch data from the database based on the selected date range
    Connection conn = koneksi.getConnection();
    try {
        String selectSQL = "SELECT * FROM laporan WHERE tanggal_transaksi BETWEEN ? AND ? ORDER BY id_transaksi DESC";
        PreparedStatement selectStmt = conn.prepareStatement(selectSQL);
        selectStmt.setDate(1, sqlStartDate);
        selectStmt.setDate(2, sqlEndDate);

        ResultSet res = selectStmt.executeQuery();
        while (res.next()) {
            DftTblModel_barang.addRow(new Object[]{
                res.getString("id_transaksi"),
                res.getString("tanggal_transaksi"),
                res.getString("total_bayar"),
                res.getString("nama_kasir"),
            });
        }

    } catch (SQLException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_filterActionPerformed

