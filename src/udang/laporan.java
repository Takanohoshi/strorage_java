public void TampilData() {
        DftTblModel_barang = new DefaultTableModel();
        DftTblModel_barang.addColumn("id transaksi");
        DftTblModel_barang.addColumn("tanggal");
        DftTblModel_barang.addColumn("total bayar");
        DftTblModel_barang.addColumn("nama");
        tabel.setModel(DftTblModel_barang);
        Connection conn = koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from laporan ORDER BY id_transaksi DESC";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_barang.addRow(new Object[]{
                    res.getString("id_transaksi"),
                    res.getString("tanggal_transaksi"),
                    res.getString("total_bayar"),
                    res.getString("nama_kasir"),
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
